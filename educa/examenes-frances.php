					<p class="text-bold2 margin-bottom-none">¿Qué son los diplomas DELF Y DALF?</p>
                   	<p class="">Los diplomas DELF (Diplôme d’Études en Langue Française) y los diplomas DALF (Diplôme Approfondi de Langue Française) son exámenes cuyo objetivo consiste en evaluar las capacidades de comprender, leer, escribir y hablar (solo o en intercambios) de un candidato en circunstancias parecidas a las de la vida real. 
Concebidos a partir del CECR (Marco Europeo Común de Referencias para las lenguas), el DELF y el DALF son los únicos diplomas oficiales de francés lengua extranjera oficialmente otorgados por el Ministerio de Educación de Francia y reconocidos internacionalmente.</p>
                    
