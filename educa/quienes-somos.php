                        <p class="bajada-tn">Y para todo ello, el <span>INSTITUTO EDUCA - IDIOMAS</span> cuenta con <b>nuevas instalaciones</b> preparadas para las necesidades de cada alumno: una amplia <b>biblioteca</b> con material actualizado, <b>libros de textos, ejercicios y complementos</b> adjuntos por nivel, <b>sala de estudio, aulas equipadas con TV/DVD, equipos de audio, wifi, pizarra inteligente, plataforma virtual, blogs para cada idioma</b>, en los cuales interactúan los docentes con los alumnos de todas las edades e interesados en la lengua. Es decir, <b>todo</b> lo necesario para que los alumnos alcancen un excelente nivel y puedan rendir con éxito los diferentes 
