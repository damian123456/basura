                        <p class="bajada-tn">El <span>INSTITUTO EDUCA - IDIOMAS</span> ofrece a sus alumnos cursos con grupos reducidos, personalizados y en una amplia y flexible franja horaria. Como así también, a aquellos que no puedan concurrir alguna vez a su curso, el instituto coloca a disposición profesores para brindarles <b>clases de apoyo, días de recuperación y tutorías individuales.</b></p> 
                        <p class="bajada-tn margin-bottom">Nuestra metodología de enseñanza está basada en la interacción creativa y participativa desde el primer día de clases. Por esa razón es que se trabaja con grupos reducidos y se brinda todos los idiomas con un mismo compromiso, que es ofrecer el mejor curso personalizado para que todo alumno desarrolle el 100% de las cuatro habilidades primordiales: la producción y la comprensión tanto oral como escrita, en el turno y horario elegido, con profesores capacitados para enseñarte una lengua extranjera, y acompañados por el soporte tecnológico apropiado. Por lo tanto, podemos decir que predomina un enfoque comunicativo, que permite a los alumnos expresarse en forma fluida, y a la vez precisa. Si bien muchos de nuestros alumnos adultos están particularmente interesados en el dominio oral del idioma, hay muchas empresas y profesionales que necesitan adquirir también el dominio de la lengua escrita. Para lograr que nuestros alumnos aprendan todas estas habilidades, utilizamos diferentes estrategias tales como: la exposición a la lengua a través de contenido multimedia, la lecto-comprensión de textos auténticos relacionados con los trabajos o profesiones de los alumnos, la discusión y resolución de problemas, el análisis de casos, el juego de roles, ejercicios de creatividad, expresión libre y otros. </p>
