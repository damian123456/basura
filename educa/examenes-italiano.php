					<p class="text-bold2 margin-bottom-none">¿Qué es el CILS?</p>
                   	<p class="">El CLM de la USC es, desde el 13 de febrero de 2007, sede de laUniversità per Stranieri de Siena para la realización de los exámenes CILS (Certificazione di Italiano come Lingua Straniera). La Università per Stranieri di Siena es una de las Universidades públicas italianas autorizadas por Ley a la enseñanza del italiano a extranjeros y a la certificación de los conocimientos de italiano como segunda lengua.<br></p>
                     El CILS (Certificazione di Italiano come lingua straniera) es su título de certificación y comprende 6 niveles, según el Common European Framework of Reference del Consejo de Europa.<br> En el Centro de Linguas Modernas de la USC, en la convocatoria del 5 de junio, se podrá realizar el examen CILS DUE-B2. La fecha de la convocatoria la establece laUniversità per Stranieri di Siena, y es inamovible.<br>El certificado CILS es un título de estudios reconocido oficialmente, el cual certifica el grado de competencia lingüístico-comunicativa en italiano como lengua extranjera. El Certificado CILS es el primer certificado de italiano en haber adoptado el sistema de  seis niveles de competencia linguistico-comunicativa propuesto por el  Marco Común Europeo de Referencia para las Lenguas del Consejo de Europa, así como el primero en haber incorporado módulos de certificación específicos para extranjeros que trabajan en Italia.<br>Está,  por tanto, considerado válido para la admisión de estudiantes extranjeros en el sistema de educación superior italiano. La superación del examen de nivel CILS Due . B2 exenta a los estudiantes extranjeros de realizar el examen de competencia lingüística en italiano requerido para la admisión en la universidad </p>
                    
