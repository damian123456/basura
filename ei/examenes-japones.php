						<p class="bajada-tn">El Examen Oficial de Nivel de Lengua Japonesa  (Nihongo nōryoku shiken, NŌKEN; JLPT en sus iniciales inglesas) tiene cinco niveles: N1, N2, N3, N4 y N5. El nivel más fácil es el N5 y el nivel más difícil es el N1.
