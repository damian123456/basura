					<p class="text-bold2 margin-bottom-none">¿Qué es el Celu?</p>
                   	<p class="">Argentina cuenta con una certificación de español de validez internacional: el CELU, Certificado de Español: Lengua y Uso. Es un certificado de dominio del español como lengua extranjera que pueden obtener todos los extranjeros cuya primera lengua no sea el español y que quieran validar su capacidad de usar este idioma como lengua segunda para ámbitos de trabajo y de estudio. El CELU es el único examen reconocido oficialmente por el Ministerio de Educación y el Ministerio de Relaciones Exteriores y Culto de la República Argentina.Posibilita estudiar en español en la universidad y trabajar en puestos que requieran el uso efectivo del español. Se acreditan dos niveles, el Intermedio y el Avanzado, que dan constancia, en distinto grado, de la capacidad lingüística del hablanteextranjero para actuar en situaciones laborales o académicas en cualquier país hispano hablante.</p>
                    
